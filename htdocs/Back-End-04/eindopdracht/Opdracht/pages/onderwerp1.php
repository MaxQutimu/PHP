            <h1>Nissan Silvia</h1>
            <p>De Nissan Silvia is een sportcoupé die tussen 1965 en 2002 werd geproduceerd door de Japanse autofabrikant Nissan. De Silvia was een vierzitter met achterwielaandrijving en werd gebouwd in zeven generaties.</p>

            <h2>Eerste generatie (1965-1968)</h2>
            <p>De eerste generatie werd ontworpen door Albrecht Graf von Goertz en had een 1,6l l4-motor van 96 pk. De auto werd uitgerust met een vierversnellingsbak. Tussen 1965 en 1968 werden er slechts 554 exemplaren geproduceerd.</p>

            <h2>Tweede generatie (1975-1979)</h2>
            <p>De tweede generatie verscheen in 1975 en was de eerste echte Silvia in massaproductie. De auto was in feite gebaseerd op Nissan Sunny die als sportieve variant diende. De productie van deze generatie eindigde in 1979.</p>

            <h2>Derde generatie (1979-1983)</h2>
            <p>De derde generatie kwam in 1979 en werd geproduceerd tot 1983. Deze generatie verscheen als coupe en liftback. De basismotor was een 1770 cc L18 viercilinder terwijl de topmotor de 2340 cc FJ24 l4-motor. Hij was zowel met een handgeschakelde vijfbak als driebak automaat te verkrijgen.</p>

            <p>In Japan werd de hatchback als Gazelle verkocht, in de VS en Canada als 200SX en Mexico als Sakura.</p>

            <h2>Vierde generatie (1983-1989)</h2>
            <p>In 1983 werd de Silvia opgevolgd door de vierde generatie. Deze verscheen opnieuw als coupe en liftback. Hij werd in Europa, Noord-Amerika en Japan verkocht. Dit model werd gebouwd in Kanda, Fukuoka. In 1986 werd de auto voorzien van een update.</p>

            <p>Gedurende zijn productieleven was de auto met vijf motoren beschikbaar: een 1,8l met of zonder turbo, 2,0l met of zonder turbo en een 3,0l V6. Daarnaast verschenen er sport-versies zoals de RS en RS-X. In de Verenigde Staten werd deze auto nogmaals verkocht als de Nissan 200SX.</p>

            <h2>Vijfde generatie (1989-1994)</h2>

            <p>Nissan Silvia (vijfde generatie)</p>
            <p>De vijfde generatie was vanaf 1989 t/m 1994 in productie geweest. Naast de coupeversie verscheen er ook een tweedeurs cabriolet. De hatchback ging verder als de 200SX. De auto was zowel met een 1,8- en 2-liter viercilinder leverbaar, allemaal met of zonder turbo. De auto van deze generatie oogde ook sportiever dan zijn voorgangers. De auto was wel iets groter geworden dan de vorige generatie.</p>

            <p>De Silvia had drie uitrustingsniveaus: J's, Q's en K's. Het topmodel beschikte over een 200 pk-sterke turbomotor welke aan een vijfversnellingsbak gekoppeld was. Deze woog niet meer dan 1200 kg. Daarnaast beschikte deze Silvia over het HICAS-systeem als optie, een door Nissan speciaal gemaakte techniek met vierwielbesturing.</p>

            <h2>Zesde generatie (1993-1998)</h2>
            <p>De zesde generatie verscheen in 1993. Deze generatie Silvia was beschikbaar met 2,0-liter en 2,4-liter motoren. De eerstgenoemde kon ook verkrijgbaar worden met turbo. Die was verkrijgbaar als Q's en K's; de J's-naam werd weggelaten.</p>

            <p>Autech</p>
            <p>Er werd ook een getunede versie van de Silvia K's gebouwd. Deze beschikte over een tweeliterturbo van 250 pk.</p>

            <h2>Zevende generatie (1999-2002)</h2>

            <p> Nissan Silvia (zevende generatie)</p>
            <p>De zevende generatie werd alleen verkocht in Japan, Australië en Nieuw-Zeeland, en was korter ten opzichte dan het vorige model. De cabriolet verscheen onder de naam Varietta, met een metalen klapdak. De Q's- en K's-varianten werden vervangen door respectievelijk de Spec-S- en Spec-R-varianten. De topversie was de Spec-R Aero, die beschikte over een achtervleugel. Die was uitgerust met een 2-liter turbomotor die 250 pk en 275 Nm produceerde. De HICAS-besturing was optioneel te verkrijgen.</p>

            <h2>Einde</h2>
            <p>In 2002 werd de productie gestopt zonder directe opvolger. De laatste exemplaren werden geleverd als Final Edition en hierna werd voor de laatste keer Silvia gebruikt.</p>
